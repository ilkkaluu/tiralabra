
package util;

public class KReuna {

    private final int ekaSolu;
  
    private final int tokaSolu;

    KReuna(int ekaSolu, int tokaSolu) {
        this.ekaSolu = ekaSolu;
        this.tokaSolu = tokaSolu;
    }

    int getEkaSolu() {
        return ekaSolu;
    }

    int getTokaSolu() {
        return tokaSolu;
    }
}
